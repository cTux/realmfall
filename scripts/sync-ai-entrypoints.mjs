import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ENTRYPOINTS = [
  {
    path: new URL('../AGENTS.md', import.meta.url),
    title: 'AI Instructions',
    notesHeading: 'Agent Notes',
  },
  {
    path: new URL('../CLAUDE.md', import.meta.url),
    title: 'Claude Instructions',
    notesHeading: 'Claude Notes',
  },
  {
    path: new URL('../.github/copilot-instructions.md', import.meta.url),
    title: 'Copilot Instructions',
    notesHeading: 'Copilot Notes',
  },
];

function renderEntrypoint({ title, notesHeading }) {
  return `<!-- Generated by \`pnpm sync:ai-entrypoints\`. -->\n\n# ${title}\n\nLoad \`docs/RULES.md\` before acting and apply only the scoped rule files under\n\`docs/rules/\` that match the task.\n\n## ${notesHeading}\n\n- Keep this file as a thin generated entrypoint. Move recurring project guidance into \`docs/RULES.md\` and \`docs/rules/\`.\n- If prompt-loading behavior changes, sync every AI instruction entrypoint in the same task.\n- Before finalizing review findings or improvement descriptions, remove the word \`still\` and describe the current behavior directly.\n- Use \`docs/WORKFLOW.md\`, \`docs/PROJECT_REVIEW.md\`, and \`docs/lore/REALMFALL.md\` as supporting references after the canonical rules are loaded.\n`;
}

for (const entrypoint of ENTRYPOINTS) {
  const entrypointPath = fileURLToPath(entrypoint.path);
  mkdirSync(dirname(entrypointPath), { recursive: true });
  writeFileSync(entrypointPath, renderEntrypoint(entrypoint));
}
